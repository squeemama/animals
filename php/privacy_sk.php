<p><strong>Subjektom údajov</strong> </p>
<p>je žijúca fyzická osoba, ktorej osobné údaje sú spracúvané. Subjektom údajov v zmysle GDPR nie je právnická osoba a na údaje, které sú spracúvané o právnickej osobe sa teda GDPR nevzťahuje, pretože údaje vzťahujúce se k právnickej osobe nie sú údajmi osobnými. GDPR nevzťahuje svoju pôsobnosť na údaje o zosnulých osobách.</p>
<p><strong>Správca a Spracovateľ</strong> </p>
<p>Správca ako aj Spracovateľ sú právnické, prípadne fyzické osoby, které osobné údaje spracúvajú.</p>
<p><strong>Správca</strong> </p> <p>(v tomto prípade Regionálne centrum SLOBODA ZVIERAT Piešťany) je tým, v koho „prospech“ sú osobné údaje spracúvané. Za ich spracúvanie je Správca osobne zodpovedný. Správca rozhoduje o účelu, spôsobe, rozsahu a metodách akými sú osobné údaje spracúvané.</p>
<p><strong>Spracovateľ </strong> </p> <p>(v tomto prípade Regionálne centrum SLOBODA ZVIERAT Piešťany a subjekty, ktoré
Regionálne centrum SLOBODA ZVIERAT Piešťany spracúvaním poverí) je oproti tomu osoba, ktorá spracúvanie osobných údajov vykonáva na základe pokynov (poverenia) a v prospech správcu. Medzi správcom a spracovateľom vzniká zmluvný vzťah. Primárne zodpovedným vo vzťahu k subjektu osobných údajov je vždy správca, nie spracovateľ. Avšak v niektorých prípadoch sa subjekt osobných údajov môže priamo obrátiť na spracovateľa.
